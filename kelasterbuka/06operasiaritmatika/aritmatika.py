#operasi aritmatika

x = 10
y = 2

#operasi penjumlahan
penjumlahan = x + y
print("hasil penjumlahan :",x, "+",y,"= ",penjumlahan)

#operasi pengurangan
pengurangan = x - y
print("hasil penguragan :",x, "-",y,"= ",pengurangan)

#operasi perkalian
perkalian = x * y
print("hasil perkalian :",x, "*",y,"= ",perkalian)

#operasi pembagian
pembagian = x / y
print("hasil pembagian :",x, "//",y,"= ",pembagian)

#operasi eksponen(pangkat)
hasil = x ** y
print("hasil exponen :",x, "pangkat",y,"= ",hasil)

#operasi modulus
hasilModulus = x % y
print("hasil modulus :",x, "+",y,"= ",hasilModulus)

#prioritas operasi, operasional precedence
